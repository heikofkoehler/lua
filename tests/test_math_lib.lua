print("=== Math Library Tests ===")

-- Basic functions
print("Basic math functions:")
print(math.sqrt(16))  -- 4
print(math.abs(-5))  -- 5
print(math.floor(3.7))  -- 3
print(math.ceil(3.2))  -- 4

-- Trigonometry
print("\nTrigonometry:")
print(math.sin(0))  -- 0
print(math.cos(0))  -- 1
print(math.tan(0))  -- 0

-- Logarithms
print("\nLogarithms:")
print(math.exp(1))  -- 2.71828...
print(math.log(2.71828))  -- ~1

-- Min/max
print("\nMin/max:")
print(math.min(5, 2, 8, 1))  -- 1
print(math.max(5, 2, 8, 1))  -- 8

-- Constants
print("\nConstants:")
print(math.pi)  -- 3.14159...
